#include <iostream>
using namespace std;
int main()
{
    int a, b;
    cout << " Enter fist  number:" << endl;
    cin >> a;

    cout << " Enter Second number:" << endl;
    cin >> b;

    cout << " sum  = " << (a + b) << endl;
    cout << " difference = " << (a - b) << endl;
    cout << " product = " << (a * b) << endl;
    cout << " division = " << (a / b) << endl;
    cout << " modulo = " << (a % b) << endl;
    return 0;
}